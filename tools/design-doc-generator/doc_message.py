"""メッセージ設計書 — 出張申請システムの画面・バッチが出力するメッセージ一覧。

バリデーションエラー（MSG9xxxx）、完了メッセージ（MSG1xxxx）、業務エラー（MSGExxxx）を
メッセージIDごとにフラットに列挙する。MSGE0001（事前承認不要）・MSGE0002（上長でない）は
業務判定の結果を利用者に返すメッセージだが、なぜその判定になるのか（Why）はここには書かず、
共通コンポーネントや処理詳細側に自然言語で埋没している。
"""
from __future__ import annotations

from common import open_template, fill_header, fill_table, save, set_print_area

TEMPLATE = "メッセージ設計書.xlsx"
OUTPUT = "メッセージ設計書.xlsx"

# ja シートの列マップ（記入見本で確認）。No.=C, メッセージID=D, メッセージ内容=I, 備考=AD
COLMAP = {"C": "no", "D": "id", "I": "content", "AD": "remark"}
START_ROW = 8

# メッセージ本体。置き換え文字列は "{0}" 形式（記入見本の記法に準拠）。
MESSAGES = [
    ("MSG90001", "必須項目が未入力です。({0})", "入力チェック（必須項目未入力）"),
    ("MSG90002", "開始日が終了日より後です。", "入力チェック（出張期間の日付逆転）"),
    ("MSG90003", "予定費用は1件以上入力してください。", "入力チェック（出張予定費用未入力）"),
    ("MSG90004", "実績金額は0以上で入力してください。", "入力チェック（出張実績登録・実績金額範囲）"),
    ("MSG90005", "実績日付は出張期間内で入力してください。", "入力チェック（出張実績登録・実績日付範囲）"),
    ("MSG10001", "事前承認が完了しました。", "完了メッセージ（事前承認画面）"),
    ("MSG10002", "事前承認を却下しました。", "完了メッセージ（事前承認却下）"),
    ("MSG10003", "経理連携が完了しました。", "完了メッセージ（経理連携ファイル作成バッチ）"),
    ("MSG10004", "出張申請を登録しました。", "完了メッセージ（出張申請登録画面・申請登録）"),
    ("MSG10005", "出張実績を登録しました。", "完了メッセージ（出張実績登録画面・実績登録）"),
    ("MSG10006", "下書きを保存しました。", "完了メッセージ（出張申請登録画面・下書き保存）"),
    ("MSG10007", "下書きを破棄します。よろしいですか？", "確認メッセージ（出張申請登録画面・下書き破棄）"),
    ("MSG10008", "下書きを破棄しました。", "完了メッセージ（出張申請登録画面・下書き破棄）"),
    ("MSG10009", "出張申請を取り消します。よろしいですか？", "確認メッセージ（出張申請一覧画面・申請取消）"),
    ("MSG10010", "出張申請を取り消しました。", "完了メッセージ（出張申請一覧画面・申請取消）"),
    ("MSG10011", "出張申請を再申請しました。", "完了メッセージ（出張申請登録画面・差し戻し再申請）"),
    ("MSGE0001", "対象の申請は事前承認が不要です。", "業務エラー（事前承認要否判定の結果、承認不要）"),
    ("MSGE0002", "承認者が申請者の上長ではありません。", "業務エラー（承認者が申請者の上長でない）"),
    ("MSGE0003", "対象の申請は実績を登録できる状態ではありません。", "業務エラー（実績登録の事前条件：事前承認済／事前承認不要のみ可）"),
    ("MSGE0004", "承認者を決定できません。申請者の上長および所属部門の部門長が未設定です。", "業務エラー（承認者決定：上長も部門長も未設定で承認者が決定できない）"),
]


def build_records() -> list[dict]:
    return [
        {"no": i + 1, "id": mid, "content": content, "remark": remark}
        for i, (mid, content, remark) in enumerate(MESSAGES)
    ]


def build() -> str:
    wb = open_template(TEMPLATE)
    ws = wb["ja"]
    fill_header(ws, product_name="メッセージ設計書")
    fill_table(ws, START_ROW, COLMAP, build_records())
    # メッセージ件数が既定印刷範囲の行数を超えるため、内容全体に張り直す。印刷右端は AI(35)。
    set_print_area(ws, right_col=35)
    return save(wb, OUTPUT)


if __name__ == "__main__":
    print("wrote", build())
